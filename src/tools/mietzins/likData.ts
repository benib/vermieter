/** Letzter Monat mit LIK-Daten (Format "YYYY-MM") */
export const LIK_LETZTER_MONAT: string =
  Object.keys(LIK_MONATSWERTE).sort().at(-1)!

/**
 * Gibt den LIK-Indexwert für einen bestimmten Monat und eine Basis zurück.
 * Falls der exakte Monat nicht vorhanden ist, wird der letzte verfügbare Monat verwendet.
 * @param monat Format "YYYY-MM"
 * @param basis Basisjahr als String (z.B. "2020")
 */
export function likWertFuerMonat(monat: string, basis: string): number | null {
  const eintrag = LIK_MONATSWERTE[monat]
  if (eintrag && eintrag[basis] !== undefined) {
    return eintrag[basis]!
  }

  // Fallback: letzten verfügbaren Monat mit dieser Basis suchen
  const keys = Object.keys(LIK_MONATSWERTE).sort()
  for (let i = keys.length - 1; i >= 0; i--) {
    if (keys[i] <= monat) {
      const val = LIK_MONATSWERTE[keys[i]][basis]
      if (val !== undefined) return val
    }
  }
  return null
}

function getEintrag(monat: string): Partial<Record<string, number>> | null {
  const eintrag = LIK_MONATSWERTE[monat]
  if (eintrag) return eintrag

  const keys = Object.keys(LIK_MONATSWERTE).sort()
  for (let i = keys.length - 1; i >= 0; i--) {
    if (keys[i] <= monat) return LIK_MONATSWERTE[keys[i]]
  }
  return null
}

/**
 * Gibt beide LIK-Werte auf einer gemeinsamen Basis zurück.
 * Verwendet direkte BFS-Daten statt Umrechnungsfaktoren, um Rundungsfehler zu vermeiden.
 * Bevorzugt die neuere gemeinsame Basis.
 */
export interface LIKPaar {
  alterWert: number
  neuerWert: number
  basis: string
  /** Neuer Wert auf der neuesten verfügbaren Basis (für Übernahme in künftige Verträge) */
  neuerWertNeueBasis: number | null
  neueBasis: string | null
}

/**
 * Gibt beide LIK-Werte auf einer gemeinsamen Basis zurück.
 * Verwendet direkte BFS-Daten statt Umrechnungsfaktoren, um Rundungsfehler zu vermeiden.
 * Bevorzugt die neuere gemeinsame Basis.
 * Liefert zusätzlich den neuen Wert auf der neuesten verfügbaren Basis.
 */
export function likWertePaarAufGemeinsamerBasis(
  monatAlt: string,
  monatNeu: string,
): LIKPaar | null {
  const eintragAlt = getEintrag(monatAlt)
  const eintragNeu = getEintrag(monatNeu)
  if (!eintragAlt || !eintragNeu) return null

  const basenAlt = Object.keys(eintragAlt)
  const basenNeu = Object.keys(eintragNeu)

  // Gemeinsame Basen finden, neueste bevorzugen
  const gemeinsam = basenAlt
    .filter((b) => basenNeu.includes(b))
    .sort((a, b) => Number(b) - Number(a))

  if (gemeinsam.length === 0) return null

  const basis = gemeinsam[0]

  // Neueste Basis des neuen Monats (für Übernahme in Zukunft)
  const neueBasisKey = basenNeu.sort((a, b) => Number(b) - Number(a))[0]
  const istAndereBasis = neueBasisKey !== basis

  return {
    alterWert: eintragAlt[basis]!,
    neuerWert: eintragNeu[basis]!,
    basis,
    neuerWertNeueBasis: istAndereBasis ? eintragNeu[neueBasisKey]! : null,
    neueBasis: istAndereBasis ? neueBasisKey : null,
  }
}
